            using (MyEshop2.Models.MyEshop2_DBEntities db = new Models.MyEshop2_DBEntities())
            {
                return (from u in db.Users
                        join r in db.Roles on
                        u.RoleID equals r.RoleID
                        where u.Username==username
                        select r.RoleName).ToArray();

